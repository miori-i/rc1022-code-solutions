// Define a function named last with one parameter: array.
// This function should:
//   assigns a number 0 to a variable named lastElement
//   assigns a value of array at index array.length
//   returns the variable lastElement

function last(array) {
  var lastElement = 0;
  lastElement = array[array.length - 1];
  return lastElement;
}

